    private void JpRecuperarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JpRecuperarMouseClicked
        String correo = txtDireccionCorreo.getText().trim();
        String nuevaContrasena = new String(JpRecuperarPassword.getPassword()).trim();

        if (correo.isEmpty() || nuevaContrasena.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Completa todos los campos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        CrearCuentaControlador controlador = new CrearCuentaControlador("usuariosinvitados");
        controlador.conectar();

        boolean actualizado = controlador.actualizarContrasenaPorCorreo(correo, nuevaContrasena);

        if (actualizado) {
            boolean enviado = enviarCorreo(correo, nuevaContrasena);
            if (enviado) {
                JOptionPane.showMessageDialog(this, "Contrase침a actualizada y enviada a tu correo.");
            } else {
                JOptionPane.showMessageDialog(this, "La contrase침a fue actualizada, pero no se pudo enviar el correo.", "Aviso", JOptionPane.WARNING_MESSAGE);
            }
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "No se encontr칩 un usuario con ese correo.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        createEmail();
        sendEmail();

        RegistroUnidadGUI r = new RegistroUnidadGUI();
        r.setVisible(true);
        this.dispose();

    }//GEN-LAST:event_JpRecuperarMouseClicked

