    private void JpEntrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JpEntrarMouseClicked
        try {
            String nombre = txtNombre.getText();
            String passIngresada = String.valueOf(pss.getPassword());

            // Verificar si la contraseña está vacía
            if (passIngresada.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "La contraseña no puede estar vacía", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Validar contraseña antes de proceder
            if (!passIngresada.equals("JGFF")) {
                JOptionPane.showMessageDialog(this, "Contraseña incorrecta", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Si pasa las validaciones anteriores, se permite continuar
            String checkQuery = "SELECT * FROM administrador WHERE Nombre = ?";
            PreparedStatement checkSt = cx.conectar().prepareStatement(checkQuery);
            checkSt.setString(1, nombre);
            ResultSet rs = checkSt.executeQuery();

            if (rs.next()) {
                // El usuario ya existe y la contraseña es válida
                System.out.println("Usuario validado: " + nombre);
                RegistroUnidadGUI registroUnidad = new RegistroUnidadGUI();
                registroUnidad.setVisible(true);
                this.dispose();
            } else {
                // Registrar nuevo usuario con contraseña válida
                int inserted = registrarUsuario(nombre, passIngresada);
                if (inserted > 0) {
                    System.out.println("Nuevo usuario " + nombre + " registrado exitosamente.");
                    RegistroUnidadGUI registroUnidad = new RegistroUnidadGUI();
                    registroUnidad.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Error al registrar usuario", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(LoginUI.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error de conexión con la base de datos: " + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_JpEntrarMouseClicked

// Método para registrar al usuario que se logueó
    private int registrarUsuario(String nombre, String password) throws SQLException {
        String insertQuery = "INSERT INTO administrador (Nombre, password) VALUES (?, ?)";
        PreparedStatement insertSt = cx.conectar().prepareStatement(insertQuery);
        insertSt.setString(1, nombre);
        insertSt.setString(2, password);
        return insertSt.executeUpdate();
    }


