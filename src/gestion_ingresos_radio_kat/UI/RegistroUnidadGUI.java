    private void cargarDatosDesdeBaseDeDatos() {
        UnidadDAO dao = new UnidadDAO();
        List<Object[]> unidades = dao.obtenerUnidades();

        DefaultTableModel model = (DefaultTableModel) jTableRegistroUnidad.getModel();
        model.setRowCount(0); // Limpiar tabla antes de cargar

        for (Object[] fila : unidades) {
            model.addRow(fila);
        }

        JOptionPane.showMessageDialog(this, "Datos cargados correctamente desde la base de datos.");
    }

