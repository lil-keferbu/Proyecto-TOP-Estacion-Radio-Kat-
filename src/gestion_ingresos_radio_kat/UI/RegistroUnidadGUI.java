    private void btnRegistrarUnidadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarUnidadActionPerformed

        String numPlaca = txtNumPlaca.getText().trim();
        String nombreProp = txtNombrePropietario.getText().trim();
        String numComercial = txtNumComercial.getText().trim();
        String numTelefono = txtNumTelefono.getText().trim();
        String tipoUnidad = (String) boxTipoUnidad.getSelectedItem();
        String estadoUnidad = (String) boxEstadoUnidad.getSelectedItem();

        if (numPlaca.isEmpty() || nombreProp.isEmpty() || numComercial.isEmpty() || numTelefono.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, completa todos los campos.", "Campos incompletos", JOptionPane.WARNING_MESSAGE);
            return;
        }

        UnidadDAO dao = new UnidadDAO();
        boolean exito = dao.registrarUnidad(numPlaca, nombreProp, numComercial, numTelefono, tipoUnidad, estadoUnidad);

        if (exito) {

            // Guardar en las variables públicas
            ultimoTipoUnidadRegistrada = tipoUnidad;
            ultimoNombrePropietarioRegistrado = nombreProp;

            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTableRegistroUnidad.getModel();
            model.addRow(new Object[]{numPlaca, nombreProp, numComercial, numTelefono, tipoUnidad, estadoUnidad});
            JOptionPane.showMessageDialog(this, "Unidad registrada exitosamente.", "Registro exitoso", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Error al registrar la unidad en la base de datos.", "Error", JOptionPane.ERROR_MESSAGE);
        }

        cargarDatosDesdeBaseDeDatos();


    }//GEN-LAST:event_btnRegistrarUnidadActionPerformed

