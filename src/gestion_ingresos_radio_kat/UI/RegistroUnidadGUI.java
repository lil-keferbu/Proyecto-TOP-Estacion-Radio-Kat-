    private void btnBuscarUnidadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarUnidadActionPerformed
        String termino = txtBuscarPlaca.getText().trim().toLowerCase();

        if (termino.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese una placa o nombre de propietario para buscar.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) jTableRegistroUnidad.getModel();
        boolean encontrado = false;

        for (int i = 0; i < model.getRowCount(); i++) {
            String placa = model.getValueAt(i, 0).toString().toLowerCase(); // columna placa
            String nombrePropietario = model.getValueAt(i, 1).toString().toLowerCase(); // columna nombre

            if (placa.contains(termino) || nombrePropietario.contains(termino)) {
                jTableRegistroUnidad.setRowSelectionInterval(i, i);
                jTableRegistroUnidad.scrollRectToVisible(jTableRegistroUnidad.getCellRect(i, 0, true));
                encontrado = true;
                break;
            }
        }

        if (!encontrado) {
            JOptionPane.showMessageDialog(this,
                    "No se encontró ninguna unidad con esa placa o nombre.",
                    "Resultado de búsqueda",
                    JOptionPane.INFORMATION_MESSAGE);
        }


    }//GEN-LAST:event_btnBuscarUnidadActionPerformed

